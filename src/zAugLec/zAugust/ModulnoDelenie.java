package zAugLec.zAugust;

class ModulnoDelenie {

    public static void main (String args[]) {

        int i = 10;
        int j = 3;

        System.out.println("i is " + i);
        System.out.println("j is " + j);

        int k = i % j;
        System.out.println("i%j is " + k);
        int d = i/j;
        System.out.println("i/j is " + d);



    //Java has one important arithmetical operator you may not be familiar with, %, also known as the modulus or
        // remainder operator. The % operator returns the remainder of two numbers.
        // For instance 10 % 3 is 1 because 10 divided by 3 leaves a remainder of 1.
        // You can use % just as you might use any other more common operator like + or -.


    }

}

